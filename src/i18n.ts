const normalizeLanguage = (value: string | null): AppLanguage => {
    return value === 'en-US' ? 'en-US' : 'zh-CN';
};

export const currentLanguage = ref<AppLanguage>(normalizeLanguage(localStorage.getItem(STORAGE_KEY)));

export const setLanguage = (language: AppLanguage) => {
    currentLanguage.value = language;
    localStorage.setItem(STORAGE_KEY, language);
};

export const t = (key: TranslationKey, replacements: Record<string, string | number> = {}) => {
    let text = messages[currentLanguage.value][key] || messages['zh-CN'][key] || key;
    Object.entries(replacements).forEach(([name, value]) => {
        text = text.split(`{${name}}`).join(String(value));
    });
    return text;
};

